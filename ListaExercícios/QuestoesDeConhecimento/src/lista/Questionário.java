public class Questionário {
	
	/*
	Questão 1 - Qual a definição de algoritmo?
	Conjunto de regras, raciocínios ou procedimentos lógicos para resolução de problemas.
	
	Questão 2 - O que é variável?
	Objeto que retêm e representa um valor ou expressão.
	
	Questão 3 - O que é tipo de variável?
	Combinação de valores e de operações que uma variável pode executar.
	
	Questão 4 - O que é atribuição?
	Definição ou re-definição do valor armazenado no local de armazenamento indicado por um nome de variável.
	
	Questão 5 - O que são operadores?
	São símbolos especiais que tem um significado próprio para a linguagem e estão associados a determinadas operações.
	
	Questão 6 - Quais as regras de definição de variáveis?
	1. Iniciar sempre com letras, jamais com números ou caracteres especiais (exceto o subtraço "_");
    2. Iniciar o nome com letra minúscula por convenção;
    3. Não duplicar nomes de variáveis, mesmo que para tipos diferentes.
    4. Não utilizar espaços em caso de nomes compostos;
    5. Não utilizar palavras reservadas da linguagem para dar nome as variáveis;
    6. Declarar o nome e o tipo da variável sempre.
	
	Questão 7 - O que é um bloco?
	Unidade fundamental que podem representar comandos, condições, objetos e muitas outras variáveis que fazem parte da construção de um programa.
	
	Questão 8 - O que é um compilador?
	É um programa de sistema que traduz um programa descrito em uma linguagem de alto nível para um programa equivalente em código de máquina para um processador.
	
	Questão 9 - O que é um código fonte?
	Conjunto de palavras ou símbolos escritos de forma ordenada, contendo instruções em uma das linguagens de programação existentes, de maneira lógica.
	
	Questão 10 - O que é executável?
	Código que pode ser executado pelo sistema operacional.
	
	Questão 11 - O que é Framework?
	É uma estrutura que contém um conjunto de códigos genéricos que tem a finalidade de prover a união entre duas partes da estrutura de uma determinada linguagem de programação.
	
	Questão 12 - O que é método Main?
	Ponto de entrada de um programa executável; é onde o controle do programa começa e termina.
	
	Questão 13 - Para que serve o ; nas instruções?
	Para separar as instruções de um programa.
	
	Questão 14 - Quais teclas de atalho são utilizadas para debugar?
	Ctrl + Shift + B = para inserir Breakpoints no debug.

    F6 = Modo debug, executar uma linha.

    F8 = Modo debug, executar até o proximo breakpoint se houver, ou até o final da execução.

    Ctrl + F2 = Para a execução atual do debug.
	
	*/
}
